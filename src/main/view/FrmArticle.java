//BUTTON RENDERER CLASS
    class ButtonRenderer extends JButton implements TableCellRenderer {

        //CONSTRUCTOR
        public ButtonRenderer() {
            //SET BUTTON PROPERTIES
            setOpaque(true);
            setFont(new Font("Poppins", 0, 11));
            setFocusPainted(false);
        }

        @Override
        public Component getTableCellRendererComponent(JTable table, Object obj,
                boolean selected, boolean focused, int row, int col) {

            //SET PASSED OBJECT AS BUTTON TEXT
            setText((obj == null) ? "" : obj.toString());

            return this;
        }
    }

//BUTTON EDITOR CLASS
    class ButtonEditor extends DefaultCellEditor {

        protected JButton btn;
        private String lbl;
        private Boolean clicked;
        private int dataRowSelected;

        public ButtonEditor(JTextField txt) {
            super(txt);

            btn = new JButton();

            btn.setOpaque(true);
            btn.setFont(new Font("Poppins", 0, 11));
            btn.setFocusPainted(false);

            //WHEN BUTTON IS CLICKED
            btn.addActionListener((ActionEvent e) -> {
                fireEditingStopped();
                generateTable();
            });
        }

        //OVERRIDE A COUPLE OF METHODS
        @Override
        public Component getTableCellEditorComponent(JTable table, Object obj,
                boolean selected, int row, int col) {

            //SET TEXT TO BUTTON,SET CLICKED TO TRUE,THEN RETURN THE BTN OBJECT
            lbl = (obj == null) ? "" : obj.toString();
            btn.setText(lbl);
            dataRowSelected = Integer.parseInt(table.getValueAt(row, 0).toString()); //Code
            clicked = true;
            return btn;
        }

        //IF BUTTON CELL VALUE CHNAGES,IF CLICKED THAT IS
        @Override
        public Object getCellEditorValue() {

            if (clicked) {
                //SHOW US SOME MESSAGE

                final ClientController clientController = new ClientController();

                final JButton btnCancel = new JButton("Cancel");

                btnCancel.setBackground(new Color(108, 117, 125, 255));
                btnCancel.setForeground(Color.WHITE);
                btnCancel.setBorder(null);
                btnCancel.setFont(new Font("Poppins", 1, 11));
                btnCancel.setFocusPainted(false);
                btnCancel.setPreferredSize(new Dimension(100, 25));

                btnCancel.addActionListener((ActionEvent e) -> {
                    JOptionPane.getRootFrame().dispose();
                });

                final JButton btnSuccess = new JButton("Ok");

                btnSuccess.setBackground(new Color(71, 201, 162, 255));
                btnSuccess.setForeground(Color.WHITE);
                btnSuccess.setBorder(null);
                btnSuccess.setFont(new Font("Poppins", 1, 11));
                btnSuccess.setFocusPainted(false);
                btnSuccess.setPreferredSize(new Dimension(100, 25));

                btnSuccess.addActionListener((ActionEvent e) -> {
                    JOptionPane.getRootFrame().dispose();
                });

                final JButton[] btnSucessOption = {btnSuccess};

                if (lbl.equalsIgnoreCase("edit")) {
                    FrmEditArticle frmEditArticle = new FrmEditArticle(dataRowSelected, FrmArticle.this);

                    parent.openModalDialog(frmEditArticle, 480, 425, 120, 40);
                } else {

                    final JButton btnDelete = new JButton("Delete");

                    btnDelete.setBackground(new Color(220, 53, 69, 255));
                    btnDelete.setForeground(Color.WHITE);
                    btnDelete.setBorder(null);
                    btnDelete.setFont(new Font("Poppins", 1, 11));
                    btnDelete.setFocusPainted(false);
                    btnDelete.setPreferredSize(new Dimension(100, 25));

                    btnDelete.addActionListener((ActionEvent actionEvent) -> {
                        articleController.deleteArticle(dataRowSelected);

                        JOptionPane.getRootFrame().dispose();

                        JOptionPane.showOptionDialog(null, "<html><div align='center'>Success</div></html>\nThe records have been successfully removed.", "Successful Dialog", 0, JOptionPane.INFORMATION_MESSAGE, null, btnSucessOption, null);
                    });

                    final JButton[] btnOptions = {btnCancel, btnDelete};

                    JOptionPane.showOptionDialog(null, "Are you sure?\nDo you really want to delete these records? This process cannot be undone.", "Confirm Dialog", 0, JOptionPane.ERROR_MESSAGE, null, btnOptions, null);

                }
            }
            //SET IT TO FALSE NOW THAT ITS CLICKED
            clicked = false;
            return lbl;
        }

        @Override
        public boolean stopCellEditing() {

            //SET CLICKED TO FALSE FIRST
            clicked = false;
            return super.stopCellEditing();
        }

        @Override
        protected void fireEditingStopped() {
            // TODO Auto-generated method stub
            super.fireEditingStopped();
        }
    }

