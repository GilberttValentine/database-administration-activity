    private void generateTableBySearch(String name) {
        List<Article> articles = articleController.findArticlesByName(name);

        tableArticles.getTableHeader().setReorderingAllowed(false);
        tableArticles.getTableHeader().setResizingAllowed(false);

        tableArticles.setModel(articleController.listArticles(articles));

        lblQuery.setText("");
        lblQuery.setText("Se encontraron " + tableArticles.getRowCount() + " resultados");

        tableArticles.getColumnModel().getColumn(5).setCellRenderer(new ButtonRenderer());
        tableArticles.getColumnModel().getColumn(5).setCellEditor(new ButtonEditor(new JTextField()));

        tableArticles.getColumnModel().getColumn(6).setCellRenderer(new ButtonRenderer());
        tableArticles.getColumnModel().getColumn(6).setCellEditor(new ButtonEditor(new JTextField()));

        tableArticles.getTableHeader().setFont(new Font("Poppins", 0, 11));

        scrollTableArticles.setBackground(Color.white);
    }

